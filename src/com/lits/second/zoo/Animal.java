package com.lits.second.zoo;

public class Animal {
    public String id;
    public String name;
    public String specie;
    public int age;

    public Animal(String id, String name, String specie, int age) {
        this.id = id;
        this.name = name;
        this.specie = specie;
        this.age = age;
    }

}
